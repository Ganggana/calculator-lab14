package lab14.sict.must.edu.mn;

/**
 * Multiplication operation implementation.
 */
public class Multiplication {

    /**
     * Multiply two double values.
     *
     * @param a first value
     * @param b second value
     * @return a * b
     */
    public double multiply(double a, double b) {
        return a * b;
    }
}
